    private void loadDataMenunggu() {
        DefaultTableModel model = (DefaultTableModel) MenungguTransaksiTabel.getModel();
        model.setRowCount(0);
        try {
            Connection conn = Koneksidatabase.getConnection();
            if (conn != null) { // Tambahkan cek null agar tidak error lagi
                String sql = "SELECT * FROM pesanan WHERE status_pembayaran = 'Menunggu Pembayaran'";
                PreparedStatement pst = conn.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();
                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getString("nama_pemesan"), rs.getString("menu"),
                        rs.getString("harga"), rs.getString("jumlah"),
                        rs.getString("total_bayar"), rs.getString("metode_pembayaran"),
                        rs.getString("status_pembayaran")
                    });
                }
            }
        } catch (SQLException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }

