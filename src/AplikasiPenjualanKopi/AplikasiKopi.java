    private void ProsesBayarButtonActionPerformed(java.awt.event.ActionEvent evt) {
        int selectedRow = MenungguTransaksiTabel.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data terlebih dahulu!");
            return;
        }

        String namaPemesan = MenungguTransaksiTabel.getValueAt(selectedRow, 0).toString();
        String totalBayar = MenungguTransaksiTabel.getValueAt(selectedRow, 4).toString();

        try {
            Connection conn = Koneksidatabase.getConnection();
            // Update status di SQLite
            String sql = "UPDATE pesanan SET status_pembayaran='Sudah Bayar' WHERE nama_pemesan=? AND total_bayar=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaPemesan);
            pst.setString(2, totalBayar);

            int rowsUpdated = pst.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Pembayaran Berhasil!");
                loadDataMenunggu();
                loadDataTerbayar();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal Update: " + e.getMessage());
        }
    }

