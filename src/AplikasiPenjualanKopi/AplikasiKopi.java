    private void KonfirmasiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KonfirmasiButtonActionPerformed
        String namaPemesan = NamaPemesanTxt.getText();
        String metodepembayaran = (String) MetodePembayaranCbx.getSelectedItem();

        StringBuilder menuBuilder = new StringBuilder();
        StringBuilder hargaBuilder = new StringBuilder();
        StringBuilder jumlahBuilder = new StringBuilder();

        int grandTotal = 0;

        // Freezy Dutch Chocolate
        if (!HargaFreezyDutchTxt.getText().isEmpty() && !JumlahFreezyDutchTxt.getText().isEmpty()) {
            int harga = Integer.parseInt(HargaFreezyDutchTxt.getText());
            int jumlah = Integer.parseInt(JumlahFreezyDutchTxt.getText());
            int total = harga * jumlah;
            grandTotal += total;

            menuBuilder.append("Freezy Dutch Chocolate").append(", ");
            hargaBuilder.append(harga).append(", ");
            jumlahBuilder.append("x").append(jumlah).append(", ");

            SubTotalFreezyTxt.setText(String.valueOf(total));
        }

        // Caramel Latte
        if (!HargaCaramelLatteTxt.getText().isEmpty() && !JumlahCaramelLatteTxt.getText().isEmpty()) {
            int harga = Integer.parseInt(HargaCaramelLatteTxt.getText());
            int jumlah = Integer.parseInt(JumlahCaramelLatteTxt.getText());
            int total = harga * jumlah;
            grandTotal += total;

            menuBuilder.append("Caramel Latte").append(", ");
            hargaBuilder.append(harga).append(", ");
            jumlahBuilder.append("x").append(jumlah).append(", ");

            SubTotalCaramelTxt.setText(String.valueOf(total));
        }

        // Americano
        if (!HargaAmericanoTxt.getText().isEmpty() && !JumlahAmericanoTxt.getText().isEmpty()) {
            int harga = Integer.parseInt(HargaAmericanoTxt.getText());
            int jumlah = Integer.parseInt(JumlahAmericanoTxt.getText());
            int total = harga * jumlah;
            grandTotal += total;

            menuBuilder.append("Americano").append(", ");
            hargaBuilder.append(harga).append(", ");
            jumlahBuilder.append("x").append(jumlah).append(", ");

            SubTotalAmericanoTxt.setText(String.valueOf(total));
        }

        // Matcha Latte
        if (!HargaMatchaLatteTxt.getText().isEmpty() && !JumlahMatchaLatteTxt.getText().isEmpty()) {
            int harga = Integer.parseInt(HargaMatchaLatteTxt.getText());
            int jumlah = Integer.parseInt(JumlahMatchaLatteTxt.getText());
            int total = harga * jumlah;
            grandTotal += total;

            menuBuilder.append("Matcha Latte").append(", ");
            hargaBuilder.append(harga).append(", ");
            jumlahBuilder.append("x").append(jumlah).append(", ");

            SubTotalMatchaTxt.setText(String.valueOf(total));
        }

        if (menuBuilder.length() > 0) {
            menuBuilder.setLength(menuBuilder.length() - 2);
            hargaBuilder.setLength(hargaBuilder.length() - 2);
            jumlahBuilder.setLength(jumlahBuilder.length() - 2);
        }

        // Tampilkan total semua
        TotalTxt.setText("Rp." + grandTotal);

        // Diskon dan total akhir
        int bayarTotal = grandTotal;
        String diskon = "0%";
        if (grandTotal > 300000) {
            diskon = "10%";
            bayarTotal = grandTotal - (grandTotal * 10 / 100);
        } else if (grandTotal > 200000) {
            diskon = "7%";
            bayarTotal = grandTotal - (grandTotal * 7 / 100);
        } else if (grandTotal > 100000) {
            diskon = "5%";
            bayarTotal = grandTotal - (grandTotal * 5 / 100);
        }
        DiskonTxt.setText(diskon);
        TotalBayarTxt.setText("Rp." + bayarTotal);

        // Tambahkan ke JTable
        DefaultTableModel model = (DefaultTableModel) MenungguTransaksiTabel.getModel();
        model.insertRow(0, new Object[]{
            namaPemesan,
            menuBuilder.toString(),
            hargaBuilder.toString(),
            jumlahBuilder.toString(),
            "Rp." + bayarTotal,
            metodepembayaran,
            "Menunggu Pembayaran"
        });

        // Simpan ke database
        try {
            Connection conn = Koneksidatabase.getConnection();
            String sql = "INSERT INTO pesanan (nama_pemesan, menu, harga, jumlah, total_bayar, metode_pembayaran, status_pembayaran) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaPemesan);
            pst.setString(2, menuBuilder.toString());
            pst.setString(3, hargaBuilder.toString());
            pst.setString(4, jumlahBuilder.toString());
            pst.setString(5, "Rp." + bayarTotal);
            pst.setString(6, metodepembayaran);
            pst.setString(7, "Menunggu Pembayaran");

            pst.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Pemesanan berhasil di konfirmasi!");
        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_KonfirmasiButtonActionPerformed

